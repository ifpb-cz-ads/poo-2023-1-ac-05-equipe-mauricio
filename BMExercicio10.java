public class BMExercicio10 {

    /* Reescreva o programa anterior utilizando o comando do-while. */

    public static void main (String[] arg) {
        int n, aux;
        n = 10;
        aux = 1;
        do {
            System.out.println(n);
            aux++;
            n++;
        } while (aux<=20);
    }
}
